#include "3-calc.h"
/**
 * op_add - add two num
 * 
 * @a : first num
 * @b : second num
 * Return: return the sum.
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - sub two num
 *
 * @a : first num
 * @b : second num
 * Return: return the sub
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply two num
 *
 * @a : first num
 * @b : second num
 * Return: return the multiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide two num
 *
 * @a : first num
 * @b : second num
 * Return: return the division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulate two num
 *
 * @a : first num
 * @b : second num
 * Return: return the modulation 
 */
int op_mod(int a, int b)
{
	return (a % b);
}
